import * as React from "react";

/**
 * Lightweight, dependency-free icon set (stroke-based, 24x24, currentColor).
 * Paths are modeled on the open-source Lucide icon set (ISC licensed).
 * Using a local set keeps the bundle small and avoids an extra dependency,
 * while giving every surface a single consistent icon language (no emoji).
 */

export type IconProps = React.SVGProps<SVGSVGElement> & {
  size?: number | string;
};

function makeIcon(
  displayName: string,
  children: React.ReactNode,
  opts: { fill?: boolean } = {}
) {
  const Comp = React.forwardRef<SVGSVGElement, IconProps>(function Icon(
    { size = 24, strokeWidth = 2, className, ...rest },
    ref
  ) {
    return (
      <svg
        ref={ref}
        xmlns="http://www.w3.org/2000/svg"
        width={size}
        height={size}
        viewBox="0 0 24 24"
        fill={opts.fill ? "currentColor" : "none"}
        stroke={opts.fill ? "none" : "currentColor"}
        strokeWidth={strokeWidth}
        strokeLinecap="round"
        strokeLinejoin="round"
        className={className}
        aria-hidden="true"
        {...rest}
      >
        {children}
      </svg>
    );
  });
  Comp.displayName = displayName;
  return Comp;
}

